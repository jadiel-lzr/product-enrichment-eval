def main() -> None:
    output_path = os.path.join(
        os.path.dirname(__file__),
        "..",
        "frontend",
        "public",
        "data",
        "enriched-noimg-claude.csv",
    )
    output_path = os.path.normpath(output_path)

    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES, extrasaction="ignore")
        writer.writeheader()
        for enrichment in ENRICHMENT_RESULTS:
            row: dict[str, str] = {field: "" for field in FIELDNAMES}
            row.update(enrichment)
            writer.writerow(row)

    print(f"Written {len(ENRICHMENT_RESULTS)} enriched products to {output_path}")

    # Summary
    with_images = sum(1 for r in ENRICHMENT_RESULTS if r.get("image_links"))
    print(f"Products with verified image URLs: {with_images}/{len(ENRICHMENT_RESULTS)}")


if __name__ == "__main__":
    main()
